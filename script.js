let currentLang = 'tig'; // Default language

function setLanguage(lang) {
    currentLang = lang;
    document.querySelectorAll('[data-translate]').forEach(element => {
        const key = element.getAttribute('data-translate');
        if (translations[lang] && translations[lang][key]) {
            // For select options, update the option text
            if (element.tagName === 'OPTION') {
                element.textContent = translations[lang][key];
            } else {
                element.textContent = translations[lang][key];
            }
        }
    });

    // Handle specific elements like the title tag
    const titleElement = document.querySelector('title');
    if (titleElement) {
        const titleKey = titleElement.getAttribute('data-translate');
        if (translations[lang] && translations[lang][titleKey]) {
            titleElement.textContent = translations[lang][titleKey];
        }
    }

    // Update footer text
    const footerTextElement = document.getElementById('footer-text');
    if (footerTextElement) {
        footerTextElement.textContent = translations[lang]["footer-text"];
    }
}

document.addEventListener('DOMContentLoaded', () => {
    const langSwitcher = document.getElementById('language-switcher');
    if (langSwitcher) {
        // Load language from URL parameter or localStorage, default to 'tig'
        const urlParams = new URLSearchParams(window.location.search);
        const langParam = urlParams.get('lang');
        const storedLang = localStorage.getItem('preferredLang');

        if (langParam && translations[langParam]) {
            currentLang = langParam;
        } else if (storedLang && translations[storedLang]) {
            currentLang = storedLang;
        } else {
            currentLang = 'tig'; // Fallback to Tigrigna
        }

        langSwitcher.value = currentLang;
        setLanguage(currentLang);

        langSwitcher.addEventListener('change', (event) => {
            const newLang = event.target.value;
            localStorage.setItem('preferredLang', newLang); // Save preference
            setLanguage(newLang);

            // Optionally update URL without reloading
            const newUrl = new URL(window.location);
            newUrl.searchParams.set('lang', newLang);
            window.history.pushState({ path: newUrl.href }, '', newUrl.href);
        });
    } else {
        // If no language switcher, still apply default language
        setLanguage(currentLang);
    }
});